// VGo — Digest view
async function renderDigest() {
  let projects = State.projects;
  if (!projects) {
    try { const res = await API.projects(); projects = res.projects; State.projects = projects; } catch(e) { projects = []; }
  }
  const today = new Date().toLocaleDateString('en-US', { weekday: 'long', month: 'long', day: 'numeric' });

  const items = projects.length ? projects.map(p => {
    const color = p.health_color || '#7C8454';
    return `<div style="margin-bottom:24px">
      <div style="display:flex;align-items:center;gap:8px;margin-bottom:10px"><span style="width:8px;height:8px;border-radius:99px;background:${color}"></span><span style="font-size:15px;font-weight:700">${esc(p.name)}</span></div>
      <ul style="margin:0;padding:0 0 0 18px;list-style:none;display:flex;flex-direction:column;gap:8px">
        <li style="position:relative;font-size:15px;line-height:1.5"><span style="position:absolute;left:-18px;top:9px;width:5px;height:5px;border-radius:99px;background:var(--muted)"></span><b style="color:#4F5635">Status:</b> ${p.health_label}, ${p.progress}% done</li>
        <li style="position:relative;font-size:15px;line-height:1.5"><span style="position:absolute;left:-18px;top:9px;width:5px;height:5px;border-radius:99px;background:var(--muted)"></span><b style="color:#25563F">Tasks:</b> ${esc(p.tasks_label || '')}</li>
        <li style="position:relative;font-size:15px;line-height:1.5"><span style="position:absolute;left:-18px;top:9px;width:5px;height:5px;border-radius:99px;background:var(--muted)"></span><b style="color:#6E5638">Due:</b> ${esc(p.due_label || 'No date')}</li>
      </ul>
    </div>`;
  }).join('') : '<p style="color:var(--muted)">No projects to summarize.</p>';

  return `
    <div class="fade-in">
      <div class="section-label">Digest</div>
      <div style="display:flex;align-items:flex-end;justify-content:space-between;margin-bottom:6px">
        <h1 class="page-title-sm">Daily standup</h1>
        <button class="btn" onclick="copyDigest()">${I.copy}Copy to share</button>
      </div>
      <p style="font-size:14px;color:var(--muted);margin:0 0 24px 0">Generated by VGo from your workspace activity · ${today}</p>
      <div class="card card-pad" style="width:100%;max-width:860px">
        <p style="font-family:var(--serif);font-size:21px;line-height:1.5;margin:0 0 26px 0;letter-spacing:-.01em;text-wrap:pretty">${projects.length} projects tracked. ${projects.filter(p => p.health === 'on_track').length} on track, ${projects.filter(p => p.health === 'at_risk').length} at risk, ${projects.filter(p => p.health === 'blocked').length} blocked.</p>
        ${items}
        <div style="display:flex;align-items:center;gap:9px;border-top:1px solid var(--border);padding-top:18px;margin-top:4px">
          <div class="ai-badge" style="width:22px;height:22px;border-radius:6px">${I.sparkle}</div>
          <span style="font-size:12.5px;color:var(--muted)">Auto-generated. Edit anything before you share.</span>
        </div>
      </div>
    </div>
  `;
}

function copyDigest() {
  const text = document.querySelector('.card.card-pad')?.innerText || '';
  navigator.clipboard.writeText(text).then(() => {
    toast('Digest copied to clipboard', 'success');
  });
}